print("Tuplas: é similar a uma lista, mas é imutável, o que significa que não pode ser alterada após sua criação e são definidas usando parênteses")

cores = ("vermelho", "verde", "azul")

# Acessando elemento usando índice
print(cores[0])  # "vermelho"

# Fatiamento slicing 
print(cores[1:3])  # ("verde", "azul")
