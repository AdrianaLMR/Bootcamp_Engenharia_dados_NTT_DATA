print("Funções relacionadas a listas: há várias funções embutidas úteis para listas")

numeros = [5, 3, 9, 1]

# len(): Retorna o número de elementos
print(len(numeros))

# max() e min(): Retornam o maior e o menor valor, respectivamente
print(max(numeros))  
print(min(numeros)) 

# sum(): Soma todos os elementos da lista (se forem numéricos).
print(sum(numeros))  # Soma todos os números da lista

# sorted(): Retorna uma nova lista ordenada.
print(sorted(numeros)) 







